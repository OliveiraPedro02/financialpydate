def previous_twentieth(date: np.datetime64, rule: Rule):
    month_date = date.astype('datetime64[M]')
    result = month_date + np.timedelta64(19, 'D')
    if result > date:
        result = (month_date - np.timedelta64(1, 'M')) + np.timedelta64(19, 'D')

    if rule in [Rule.CDS_2015, Rule.old_CDS, Rule.CDS, Rule.Twentieth_IMM]:
        skip_months = month(result) % 3
        if skip_months != 0:
            result = (result.astype('datetime64[M]') - np.timedelta64(skip_months, 'M')) + +np.timedelta64(19, 'D')

    return result


def cds_maturity(date: np.datetime64, period: np.timedelta64, rule: Rule) -> str | None:
    anchor_date = previous_twentieth(date, rule)
    if rule == Rule.CDS_2015 and (month(anchor_date) in [6, 12]):
        if np.int_(period) == 0:
            return None
        else:
            anchor_date = (anchor_date.astype('datetime64[M]') - np.timedelta64(3, 'M')) + np.timedelta64(19, 'D')
    maturity = ((anchor_date.astype('datetime64[M]') + np.timedelta64(3, 'M')) + period) + np.timedelta64(19, 'D')

    if maturity > date:
        return str(maturity)

    raise ValueError(f'Maturity {maturity} is past the trade date {date}')


